    <div class="text-white p-4 text-3xl pb-5">
      The Pomodoro Technique, è una soluzione molto efficace per gestire il
      tempo. Inventata negli anni ’80 da Francesco Cirillo, uno sviluppatore di
      software, deve il suo nome ad un timer a forma di pomodoro utilizzato dal
      creatore, e sul quale si basa l’intera tecnica.<br /><br /> In pratica, la
      Tecnica del Pomodoro si basa su un concetto molto semplice: durante i
      minuti dedicati al lavoro, scanditi dal timer, devi fare esclusivamente
      quell’operazione, senza distrarti per guardare il cellulare oppure
      controllare le e-mail. Devi solo studiare, o lavorare.<br /><br /> Lo
      schema della Tecnica del Pomodoro è molto semplice ed immediato:
      <br />
      <br />
      <ul class="list-disc list-inside">
        <li>Stabilisci i compiti da svolgere durante la giornata;</li>
        <li>Lavora per 25 minuti senza distrazioni;</li>
        <li>Quando il timer segna la fine dei 25 minuti fermati;</li>
        <li>Fai una pausa di 5 minuti;</li>
        <li>Ricomincia a lavorare per altri 25 minuti.</li>
        <li>
          Ogni 4 sessioni (ovvero ogni due ore) prenditi una pausa di 15/30
          minuti.
        </li>
      </ul>
